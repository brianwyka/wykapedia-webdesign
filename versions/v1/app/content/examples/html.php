	<h3>Hypertext Markup Language</h3>
    <p>
    	Hypertext Markup Language, or HTML, is responsible for the structure and layout of the page.  You can think of it as a
        tree structure as demonstrated below.  The tree strucutre not only allows the CSS to control the look and feel of the page, 
        but it keeps the document well-formed and organized.
    </p>
