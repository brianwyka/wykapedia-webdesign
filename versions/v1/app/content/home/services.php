<h3>Quality Assurance</h3>
<p>
	Webpage quality can be measured by the following 3 points:
</p>
<ul>
    <li>
        <span class="b">Browser Compatibility</span><br />
        This refers to a webpage looking and performing the same across all browser versions and providers, such as 
        Google Chrome, Mozilla Firefox, Opera, Safari, and different versions of Internet Explorer.
    </li>
    <li>
        <span class="b">Web Standards Compliance</span><br />
        Web standards are put forth by <a href="http://www.w3.org/">W3C</a> (the World Wide Web Consortium).  The standards
        are a list of rules and specifications which govern the world wide web.
    </li>
    <li>
        <span class="b">Structure and Organization</span><br />
        Structure and organization relies on the two above.  It is important to have a webpage that not only looks good, 
        but also is organized and consistent.
    </li>
</ul>
<p>
	All of our websites that we provide to our clients are governed by these 3 points.
</p>